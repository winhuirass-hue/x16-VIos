# Nano File Manager for DOS with FAT12 Support

## Features:
- Change Disk Functionality
- Help Menu

## File Manager Operations:
1. **Change Disk:** Select a different disk drive (A:, B:, etc.)
2. **Help Menu:** Access the help menu for assistance

### Code Example:
```asm
; Enter your assembly code for the nano file manager here.
start:
    ; Code for selecting disk and displaying help
    jmp start  ; Loop back to start
```

## Instructions:
- Use the arrow keys to navigate.
- Press 'H' for help.
- Press 'C' to change the disk.
